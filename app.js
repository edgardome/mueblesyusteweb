document.addEventListener('DOMContentLoaded', () => {
  let currentLang = localStorage.getItem('preferredLang') || 'es';

  const translateCategory = (cat) => {
    if (currentLang === 'es') return cat;
    return translations.en.categories[cat] || cat;
  };

  const updateLanguage = (lang) => {
    currentLang = lang;
    localStorage.setItem('preferredLang', lang);
    
    // Update all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (translations[lang][key]) {
        el.textContent = translations[lang][key];
      }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (translations[lang][key]) {
        el.placeholder = translations[lang][key];
      }
    });

    // Update active class on buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    // Update specific instances like category titles in projects page
    if (window.renderProjects) {
        const catTitle = document.getElementById('current-category-title');
        const originalCat = catTitle.dataset.cat || 'Todos';
        if (originalCat === 'Todos') {
            catTitle.textContent = translations[lang].projects_all;
        } else {
            catTitle.textContent = translateCategory(originalCat);
        }
        window.renderProjects(originalCat);
    }
  };

  // Language switcher event listeners
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      updateLanguage(btn.getAttribute('data-lang'));
    });
  });

  // Initial call
  updateLanguage(currentLang);

  // Setup scroll animations
  const observerOptions = {
    threshold: 0.1,
    rootMargin: "0px 0px -50px 0px"
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, observerOptions);

  const initScrollAnimations = () => {
    const elementsToAnimate = document.querySelectorAll('.animate-on-scroll');
    elementsToAnimate.forEach(el => {
      el.classList.remove('visible'); // reset
      observer.observe(el);
    });
  };

  initScrollAnimations();

  // ====== Proyectos Logic ======
  if (typeof projectsData !== 'undefined' && document.getElementById('projects-grid')) {
    
    const catList = document.getElementById('category-list');
    const projectsGrid = document.getElementById('projects-grid');
    const catTitle = document.getElementById('current-category-title');
    
    // Modal elements
    const modal = document.getElementById('project-modal');
    const modalClose = document.getElementById('modal-close');
    const modalTitle = document.getElementById('modal-title');
    const modalDesc = document.getElementById('modal-desc');
    const modalGallery = document.getElementById('modal-gallery');

    // Extract unique categories
    const categoriesSet = [...new Set(projectsData.map(p => p.categoryName))];
    
    // Populate categories
    const populateCategories = () => {
        catList.innerHTML = '';
        const allLi = document.createElement('li');
        allLi.textContent = translations[currentLang].projects_all;
        allLi.dataset.cat = 'Todos';
        if (catTitle.dataset.cat === 'Todos' || !catTitle.dataset.cat) {
            allLi.classList.add('active-cat');
        }
        catList.appendChild(allLi);

        categoriesSet.forEach(cat => {
            const li = document.createElement('li');
            li.textContent = translateCategory(cat);
            li.dataset.cat = cat;
            if (catTitle.dataset.cat === cat) {
                li.classList.add('active-cat');
            }
            catList.appendChild(li);
        });
    };

    window.renderProjects = (category) => {
      projectsGrid.innerHTML = ''; // clear
      catTitle.dataset.cat = category;
      
      const filtered = category === 'Todos' 
        ? projectsData 
        : projectsData.filter(p => p.categoryName === category);

      if (filtered.length === 0) {
        projectsGrid.innerHTML = `<p style="color:var(--text-muted);">${currentLang === 'es' ? 'No hay proyectos en esta categoría.' : 'No projects in this category.'}</p>`;
        return;
      }

      filtered.forEach(proj => {
        const coverImage = (proj.images && proj.images.length > 0) 
            ? proj.images[0] 
            : 'assets/mesa.jpg';

        const displayTitle = currentLang === 'en' ? (proj.title_en || proj.title) : proj.title;
        const displayDesc = currentLang === 'en' ? (proj.description_en || proj.description) : proj.description;

        const card = document.createElement('div');
        card.className = 'card animate-on-scroll';
        card.innerHTML = `
          <div class="card-img-wrapper">
             <img src="${coverImage}" alt="${displayTitle}">
          </div>
          <div class="card-content">
            <h3 style="font-size: 1.1rem; text-align: center;">${displayTitle}</h3>
            <p style="font-size: 0.85rem;">${displayDesc ? displayDesc.substring(0, 100) + '...' : ''}</p>
            <a href="#" class="card-link" data-id="${proj.id}" style="width: 100%; justify-content: center;">${translations[currentLang].details} &rarr;</a>
          </div>
        `;
        projectsGrid.appendChild(card);
      });

      populateCategories();
      initScrollAnimations();
    };

    // Initial render
    window.renderProjects('Todos');

    catList.addEventListener('click', (e) => {
      if (e.target.tagName === 'LI') {
        const cat = e.target.dataset.cat;
        catTitle.textContent = cat === 'Todos' ? translations[currentLang].projects_all : cat;
        window.renderProjects(cat);
      }
    });

    projectsGrid.addEventListener('click', (e) => {
      if (e.target.classList.contains('card-link')) {
        e.preventDefault();
        const id = e.target.dataset.id;
        const project = projectsData.find(p => p.id === id);
        
        if (project) {
          const displayTitle = currentLang === 'en' ? (project.title_en || project.title) : project.title;
          const displayDesc = currentLang === 'en' ? (project.description_en || project.description) : project.description;
          
          modalTitle.textContent = displayTitle || 'Project';
          modalDesc.textContent = displayDesc || (currentLang === 'es' ? 'Sin descripción detallada.' : 'No detailed description.');
          
          modalGallery.innerHTML = '';
          if (project.images && project.images.length > 0) {
            project.images.forEach(img => {
               const imgEl = document.createElement('img');
               imgEl.src = img;
               modalGallery.appendChild(imgEl);
            });
          }

          modal.classList.add('active');
        }
      }
    });

    modalClose.addEventListener('click', () => {
      modal.classList.remove('active');
    });

    modal.addEventListener('click', (e) => {
      if (e.target === modal) {
        modal.classList.remove('active');
      }
    });
  }
});
